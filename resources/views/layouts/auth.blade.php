    document.addEventListener("DOMContentLoaded", function () {
        // Attach event listeners to all <code> elements
        let codeElements = document.querySelectorAll("code");

        codeElements.forEach(function (element) {
            element.style.cursor = "pointer";
            element.style.padding = "0.1rem 0.3rem";
            element.style.backgroundColor = "#fafafa";
            element.addEventListener("click", function () {
                copyToClipboard(element.innerHTML);
            });

            element.addEventListener("mouseover", function () {
                element.style.backgroundColor = "#eaeaea";
            });

            element.addEventListener("mouseout", function () {
                element.style.backgroundColor = "#fafafa";
            });
        });
    });

    function copyToClipboard(text) {
        let textarea = document.createElement("textarea");
        textarea.textContent = text;
        textarea.style.position = "fixed";  // Prevents scrolling to bottom of page in MS Edge.
        document.body.appendChild(textarea);
        textarea.select();

        try {
            document.execCommand("copy");  // The command to copy the text to clipboard
        } catch (err) {
            console.error("Unable to copy", err); // Handle any errors that might occur
        }

        document.body.removeChild(textarea); // Remove the temporary element
    }

